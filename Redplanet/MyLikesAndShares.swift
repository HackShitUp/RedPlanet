class MyLikesAndShares: UITableViewController, UINavigationControllerDelegate {
    
    
    
    // Array to hold likes
    var likedObjects = [PFObject]()
    
    
    // Function to get likes and shares
    func fetchLikes(sender: Any) {
        let likes = PFQuery(className: "Likes")
        likes.whereKey("fromUser", equalTo: PFUser.current()!)
        likes.findObjectsInBackground(block: {
            (objects: [PFObject]?, error: Error?) in
            if error == nil {
                
                // Dismiss Progress
                SVProgressHUD.dismiss()
                
                // Clear array
                self.likedObjects.removeAll(keepingCapacity: false)
                
                for object in objects! {
                    // Append
                    self.likedObjects.append(object)
                }
                
                
            } else {
                print(error?.localizedDescription as Any)
                
                // Dismiss Progress
                SVProgressHUD.dismiss()
            }
            
            // Reload data
            self.tableView!.reloadData()
        })
    }
    
    

